"use strict";
// Interface:
// Một interface là một tập hợp các phương thức trừu tượng mà một lớp phải triển khai (implement).
// Một class có thể triển khai nhiều interface.
// Một interface không thể chứa các phương thức cụ thể, chỉ có thể là các phương thức trừu tượng.
// Interface cung cấp một cách tiêu chuẩn để các lớp khác nhau tương tác với nhau.
// Abstract Class:
// Một abstract class là một lớp mà bạn không thể tạo một thể hiện (instance) trực tiếp từ nó.
// Một abstract class có thể chứa cả các phương thức trừu tượng (abstract methods) và các phương thức cụ thể (concrete methods).
// Một class có thể kế thừa từ một abstract class bằng cách triển khai (implement) tất cả các phương thức trừu tượng của nó.
// Một abstract class cung cấp một khung (framework) cho các lớp con để triển khai và mở rộng.
